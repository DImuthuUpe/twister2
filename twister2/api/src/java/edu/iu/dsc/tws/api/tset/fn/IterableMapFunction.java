package edu.iu.dsc.tws.api.tset.fn;

/**
 * Iterable map function
 *
 * @param <I> input type
 * @param <O> output type
 */
public interface IterableMapFunction<I, O> extends TFunction {
  /**
   * Map with an iterable input
   *
   * @param t type of input
   * @return object
   */
  O map(Iterable<I> t);
}
